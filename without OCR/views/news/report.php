        <td style="text-align: justify;">
        <p> SHAH ALAM: Pengasas dan Pengerusi Eksekutif Kumpulan KK Mart, Datuk Seri Dr Chai Kee Kan dan isteri, Datin Seri Loh Siew Mui dilepas dan dibebaskan (DNA) oleh Mahkamah Sesyen di sini hari ini, daripada pertuduhan melukakan perasaan orang Islam dalam isu penjualan stoking tertera kalimah Allah. <br>
        <br>Israel terus bedil GazaSelain itu, tiga pengarah syarikat pembekal stoking berkenaan, Xin Jian Chang Sdn Bhd turut mendapat perintah DNAA selepas pendakwaan tidak mahu meneruskan pertuduhan ke atas lima individu berkenaan.<br>
        <br>Untuk rekod, dua syarikat dan lima individu ini mengemukakan representasi pada 17 Mei lalu kepada Jabatan Peguam Negara untuk menggugurkan kes berkenaan.<br>
        <br>Pada 26 Mac lalu, Kee Kan dan Siew Mui yang juga Pengarah KK Super Mart & Superstore mengaku tidak bersalah atas masing-masing satu pertuduhan, sengaja melukakan perasaan orang Islam dengan mempamerkan stoking tertera kalimah Allah di rak pameran jualan, yang telah dilihat oleh seorang lelaki berusia 32 tahun.<br>
        <br>Mereka didakwa melakukan kesalahan itu di KK Supermart & Superstore Sdn Bhd, Bandar Sunway pada jam 6.30 pagi 13 Mac lalu, mengikut Seksyen 298 Kanun Keseksaan yang boleh membawa hukuman penjara sehingga setahun atau denda atau dengan kedua-duanya, jika sabit kesalahan.<br>
        <br>Kee Kan yang juga mewakili syarikatnya KK Supermart & Superstore turut didakwa atas pertuduhan sama.<br>
        <br>Sementara itu, syarikat pembekal yang mengedar barangan kepada KK Super Mart iaitu Xin Jian Chang Sdn Bhd bersama tiga pengarahnya, didakwa atas masing-masing satu pertuduhan bersubahat melukakan perasaan orang Islam dalam isu berkaitan stoking tertera kalimah Allah pada tempat, waktu dan tarikh yang sama.<br>
        <br>Pembekal itu ialah tiga beranak, suami, Soh Chin Huat, 61; isterinya, Goh Li Huay, 62; dan anak mereka, Soh Hui San, 36;<br>
        <br>Kesemua mereka didakwa mengikut Seksyen 109 Kanun Keseksaan yang dibaca bersama Seksyen 298 kanun sama yang memperuntukkan hukuman penjara maksimum satu tahun atau denda atau kedua-duanya, jika sabit kesalahan.</p>
