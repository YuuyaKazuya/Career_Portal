        <p>GAZA: Serangan udara dan tembakan artileri tentera Israel terus menggegarkan Semenanjung Gaza semalam, lebih sembilan bulan sejak perang tercetus 7 Oktober lalu, menjadikan harapan untuk keamanan di wilayah terkepung itu semakin pudar.<br>
        <br>Peluru menghujani kawasan kejiranan Tal Al-Hawa, Sheikh Ajlin dan Al-Sabra di Gaza City, wartawan AFP melaporkan, manakala saksi berkata tentera Israel telah membedil kawasan Al-Mughraqa dan pinggir utara kem pelarian Nuseirat di tengah Gaza.<br>
        <br>Sumber hospital pula melaporkan tiga maut akibat serangan ke atas sebuah rumah di bandar Deir al-Balah, juga di tengah-tengah wilayah pantai, manakala paramedik dari Bulan Sabit Merah Palestin berkata mereka membawa keluar lima mayat susulan serangan udara Israel berdekatan kem Al-Maghazi.<br>
        <br>Di selatan Gaza, saksi melaporkan tembakan artileri dan serangan helikopter di timur bandar Khan Yunis dan kawasan barat Rafah, berhampiran sempadan Mesir.<br>
        <br>Tentera Israel berkata ia meneruskan aktivitinya di seluruh wilayah itu dan telah melakukan serangan di Rafah dan tengah Gaza yang membunuh sebilangan pejuang Hamas, serta serangan udara di seluruh kawasan sepanjang hari.<br>
        <br>Minggu lalu, Presiden Amerika Syarikat Joe Biden mencadangkan perjanjian gencatan senjata mungkin hampir pada sidang kemuncak Pertubuhan Perjanjian Atlantik Utara (NATO).<br>
        <br>Beliau berkata kedua-dua pihak telah bersetuju dengan rangka kerja yang telah ditetapkannya pada akhir Mei.<br>
        <br>Semalam Hamas mengecam Amerika Syarikat (AS) kerana menyokong pembunuhan beramai-ramai melalui bekalan senjatanya ke Israel.<br>
        <br>AS membekalkan Israel dengan bantuan ketenteraan setiap tahun bernilai berbilion dolar.<br>
        <br>Peperangan menyebabkan hampir seluruh penduduk wilayah terkepung itu kehilangan tempat tinggal, kebanyakan mereka telah mencari perlindungan di sekolah, namun enam daripadanya menjadi sasaran dalam serangan sejak 6 Julai lalu.<br>
        <br>Kementerian kesihatan Gaza berkata jumlah korban akibat serangan kelmarin di sebuah sekolah kendalian PBB di Nuseirat meningkat kepada 22 orang.<br>
        <br>Dalam kejadian lain, seorang pejuang Hizbullah dan adik perempuannya terbunuh ketika serangan Israel ke atas Bint Jbeil, selatan Lubnan, menurut Agensi Berita Kebangsaan Lubnan dan Hizbullah.</p>
