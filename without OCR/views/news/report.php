        <p>KULIM: Seorang lelaki berusia 33 tahun yang direman bagi membantu siasatan berhubung kejadian dua beradik maut akibat termakan keropok mengandungi racun, dibebaskan dengan jaminan polis.<br>
        <br>Ketua Polis Daerah Kulim, Superintendan Mohd Azizul Mohd Khairi, berkata suspek yang merupakan seorang pekebun itu, direman sejak 11 Julai hingga semalam.<br>
        <br>"Tempoh reman tidak akan disambung dan suspek dibebaskan dengan jaminan polis sementara menunggu keputusan Timbalan Pendakwa Raya.<br>
        <br>"Kertas siasatan sudah lengkap dan diserahkan kepada Timbalan Pendakwa Raya," katanya dalam kenyataan hari ini.<br>
        <br>Media sebelum ini melaporkan dua beradik Muhammad Akil Syauqi Nur Sufyan, 3, dan adiknya Muhammad Luth Syauqi, 2, meninggal dunia selepas makan keropok mengandungi racun tikus.<br>
        <br>Dalam kejadian 7 Julai di kawasan kebun di Kampung Padang Ubi, Labu Besar dekat sini, kanak-kanak tersebut dipercayai mengambil peket keropok yang digantung pada dawai besi pagar kebun oleh pemilik kebun bagi memerangkap monyet.<br>
        <br>Muhammad Akil Syauqi dilaporkan meninggal dunia di Hospital Pulau Pinang (HPP) 10 Julai, manakala Muhammad Luth Syauqi meninggal dunia dua hari kemudian di hospital sama.<br>
        <br>Jenazah dua beradik itu dikebumikan di Tanah Perkuburan Islam Kampung Banggol Berangan, Baling.<br>
        <br>Siasatan mengikut Seksyen 31(1)(a) Akta 611 berkaitan pengabaian terhadap kanak-kanak dan Seksyen 284 Kanun Keseksaan.</p>
